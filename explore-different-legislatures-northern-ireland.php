			<h2 id="history-of-ni">The history of primary legislation for Northern Ireland</h2>
			<p>The Parliament of Northern Ireland was established in 1921 and passed a few hundred “Acts” before it was suspended in 1972. In 1973, a new legislature - called (like the present body) the Northern Ireland Assembly - was set up, but this body passed just four “Measures” before being formally dissolved in 1975.</p>
			<p>The present Northern Ireland Assembly was set up following the signing of the Belfast Agreement on Good Friday on 10 Apr 1998. The Northern Ireland Act 1998 then devolved power to make legislation for Northern Ireland, in the form of “Acts”, to the new Assembly. The power to legislate on certain matters was retained by the UK Parliament. Having been suspended briefly on previous occasions, the Assembly was suspended in October 2002 and remained suspended for nearly four-and-a-half years. The Assembly and devolved government were restored on 8 May 2007.</p>
			<p>During the periods when devolved government has been in abeyance, and during periods of suspension of the present Assembly, legislation has been made for Northern Ireland by the UK government. This has been effected by means of “Orders in Council”, that is to say, orders made by the Queen on the advice of her Privy Council.</p>
			<p>All the legislation for Northern Ireland mentioned above, so far as remaining in force and including consolidated revisions, was brought together in the numbered volumes of the publication The Northern Ireland Statutes Revised (NISR). NISR also included four volumes (A to D) containing pre-1921 Acts of the UK Parliament and its predecessor parliaments extending to Northern Ireland. Produced by the staff of the Northern Ireland Statutory Publications Office (NISPO) in Belfast, NISR was the official revised version of the primary legislation of Northern Ireland. The revised legislation of Northern Ireland continues to be maintained in Belfast by NISPO staff.</p>
