			<p>Most of the Acts passed by UK Parliament are 'Public General Acts', which deal with matters of general public interest. UK Parliament also passes a small number of 'Private Acts', which are sub-divided into 'Local Acts' (relating to matters of local interest) and 'Personal Acts' (relating to particular persons - nowadays, these are very rare). Public and General Acts passed by the UK Parliament provide laws for the whole of the UK (England, Wales, Scotland and Northern Ireland). There are still many Acts in force that were enacted by the parliaments of the separate countries that co-existed in the British Isles before the United Kingdom of Great Britain and Ireland was formed in 1801.</p>
